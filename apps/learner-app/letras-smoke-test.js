const { io } = require('socket.io-client');

const apiBase = 'http://localhost:3000';

async function request(path, init = {}) {
  const res = await fetch(`${apiBase}${path}`, {
    ...init,
    headers: {
      'Content-Type': 'application/json',
      ...(init.headers || {}),
    },
  });

  const text = await res.text();
  let data;
  try {
    data = text ? JSON.parse(text) : null;
  } catch {
    data = text;
  }

  if (!res.ok) {
    throw new Error(`${init.method || 'GET'} ${path} failed: ${res.status} ${JSON.stringify(data)}`);
  }

  return data;
}

function waitForEvent(socket, eventName, predicate, timeoutMs = 7000) {
  return new Promise((resolve, reject) => {
    const timeout = setTimeout(() => {
      socket.off(eventName, handler);
      reject(new Error(`Timeout waiting for ${eventName}`));
    }, timeoutMs);

    const handler = (payload) => {
      if (!predicate || predicate(payload)) {
        clearTimeout(timeout);
        socket.off(eventName, handler);
        resolve(payload);
      }
    };

    socket.on(eventName, handler);
  });
}

function connectSocket({ learnerProfileId, participantId, role }) {
  const socket = io('http://localhost:3000/realtime', {
    transports: ['websocket'],
    query: { learnerProfileId, participantId, role },
  });

  return new Promise((resolve, reject) => {
    const timeout = setTimeout(() => {
      socket.disconnect();
      reject(new Error(`Timeout connecting socket (${role})`));
    }, 7000);

    socket.on('connect', () => {
      clearTimeout(timeout);
      resolve(socket);
    });

    socket.on('connect_error', (error) => {
      clearTimeout(timeout);
      reject(error);
    });
  });
}

(async () => {
  const random = Math.random().toString(36).slice(2, 8);
  const learnerDisplayName = `Smoke Learner ${random}`;

  const health = await request('/health');
  if (!health || health.status !== 'ok') {
    throw new Error('Health endpoint did not return status ok');
  }

  const themes = await request('/themes');

  if (!Array.isArray(themes) || themes.length === 0) {
    throw new Error('No themes available from /themes');
  }

  const learner = await request('/learners', {
    method: 'POST',
    body: JSON.stringify({
      displayName: learnerDisplayName,
      notes: 'Generated by smoke test',
    }),
  });

  const learnerProfileId = learner.id;
  const theme = themes[0];

  await request(`/learners/${learnerProfileId}/themes`, {
    method: 'POST',
    body: JSON.stringify({ themeId: theme.id }),
  });

  const assignedThemes = await request(`/learners/${learnerProfileId}/themes`);

  const session = await request('/sessions', {
    method: 'POST',
    body: JSON.stringify({
      learnerProfileId,
      deviceId: `smoke-device-${random}`,
      role: 'learner',
    }),
  });

  await request(`/sessions/${learnerProfileId}/state`, {
    method: 'PATCH',
    body: JSON.stringify({
      currentView: 'LearnerHome',
      statePayload: { smoke: true },
    }),
  });

  await request(`/sessions/${learnerProfileId}/lock`, {
    method: 'PUT',
    body: JSON.stringify({ isLocked: true }),
  });

  const units = await request(`/learning-content/units?themeId=${theme.id}`);
  if (!Array.isArray(units) || units.length === 0) {
    throw new Error('No units returned for selected theme');
  }

  const activities = await request(`/learning-content/units/${units[0].id}/activities`);
  if (!Array.isArray(activities) || activities.length === 0) {
    throw new Error('No activities returned for selected unit');
  }

  await request('/progress', {
    method: 'POST',
    body: JSON.stringify({
      learnerProfileId,
      activityId: activities[0].id,
      status: 'COMPLETED',
      score: 100,
      elapsedSeconds: 30,
    }),
  });

  const progress = await request(`/progress/${learnerProfileId}`);

  const learnerSocket = await connectSocket({
    learnerProfileId,
    participantId: `learner-${random}`,
    role: 'learner',
  });

  const educatorSocket = await connectSocket({
    learnerProfileId,
    participantId: `educator-${random}`,
    role: 'educator',
  });

  const presencePromise = waitForEvent(learnerSocket, 'presence_changed', (payload) => {
    return payload?.learnerProfileId === learnerProfileId && payload.educatorsOnline?.length >= 1;
  });

  const helpRequestedPromise = waitForEvent(educatorSocket, 'help_requested', (payload) => {
    return payload?.learnerProfileId === learnerProfileId;
  });

  const lockChangedTruePromise = waitForEvent(learnerSocket, 'locked_changed', (payload) => {
    return payload?.learnerProfileId === learnerProfileId && payload.isLocked === true;
  });

  const lockChangedFalsePromise = waitForEvent(learnerSocket, 'locked_changed', (payload) => {
    return payload?.learnerProfileId === learnerProfileId && payload.isLocked === false;
  });

  const helpReceivedPromise = waitForEvent(learnerSocket, 'help_received', (payload) => {
    return payload?.learnerProfileId === learnerProfileId;
  });

  learnerSocket.emit('help_requested', {
    learnerProfileId,
    message: 'Need help in smoke test',
  });

  educatorSocket.emit('lock_set', { learnerProfileId });

  const presencePayload = await presencePromise;
  const helpRequestedPayload = await helpRequestedPromise;
  const lockTruePayload = await lockChangedTruePromise;

  educatorSocket.emit('lock_release', { learnerProfileId });
  const lockFalsePayload = await lockChangedFalsePromise;

  educatorSocket.emit('help_received', {
    learnerProfileId,
    message: 'Help acknowledged in smoke test',
  });

  const helpReceivedPayload = await helpReceivedPromise;

  learnerSocket.disconnect();
  educatorSocket.disconnect();

  const summary = {
    ok: true,
    learnerProfileId,
    themeAssigned: assignedThemes.length,
    sessionId: session.id,
    unitsFetched: units.length,
    activitiesFetched: activities.length,
    progressRows: progress.length,
    realtime: {
      presenceEducatorsOnline: presencePayload.educatorsOnline.length,
      helpRequestedMessage: helpRequestedPayload.message,
      lockSetState: lockTruePayload.isLocked,
      lockReleaseState: lockFalsePayload.isLocked,
      helpReceivedMessage: helpReceivedPayload.message,
    },
  };

  console.log(JSON.stringify(summary, null, 2));
})().catch((error) => {
  console.error(error.message || error);
  process.exit(1);
});
